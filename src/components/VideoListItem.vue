<template>
    <li @click="onVideoSelect" class="list-group-item"> 
        <img class="mr-3 " :src="thumbnailUrl">
        <div class="media-body">
            {{video.snippet.title}}
        </div>
    </li>
</template>

<script>
export default {
    name: "VideoListItem",
    props: ['video'],
    methods: {
        onVideoSelect() {
            this.$emit('videoSelect', this.video)
        }
    },
    computed: {
        thumbnailUrl() {
            return this.video.snippet.thumbnails.default.url
        }
    }
}
</script>

<style scoped>
     li {
         display: flex;
         cursor: pointer;
     }

     li:hover {
         background: #eee;
     }    
</style>